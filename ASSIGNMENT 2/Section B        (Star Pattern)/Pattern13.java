class Pattern13{

      public static void main(String [] args)
       {
		    for(int i1=0;i1<5;i1++)
		      {
		      for(int j1=0;j1<=i1;j1++)
			  {
			  System.out.print(" ");
			  }
			  for (int k1=4;k1>=i1;k1--)
			  {  
              System.out.print("*");
			  }
	          
			  System.out.println();
		      }
	          for(int i=1;i<5;i++)
		       {
		      for(int j=4;j>=i;j--)
			  {
			  System.out.print(" ");
			  }
			  for(int x=0;x<=i;x++)
			  {
			  System.out.print("*");
			  }
			
			  System.out.println();
		      }
			  
	          
		   }
		}
    
		  

    	
    